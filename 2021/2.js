const { reduce } = require("lodash");

/*
down X increases your aim by X units.
up X decreases your aim by X units.
forward X does two things:
It increases your horizontal position by X units.
It increases your depth by your aim multiplied by X.
*/

const getLocationData = (moveArray) => reduce(moveArray, (currentStatus, currentMove) => {
  var { aim, depth, distance } = currentStatus;
  const command = currentMove.split(' ')[0];
  const change = parseInt(currentMove.split(' ')[1]);
  if (command === 'down') {
    aim += change;
  }
  if (command === 'up') {
    aim -= change;
  }
  if (command === 'forward') {
    distance += change;
    depth += aim * change;
  }
  return { aim, depth, distance };
}, {
  aim: 0,
  depth: 0,
  distance: 0,
});

